# ======================================================
# STEP 2: Combine into one dataframe
# ======================================================
import pandas as pd
from sklearn.metrics import mean_squared_error, mean_absolute_error, r2_score

# ======================================================
# STEP 1: Prepare your data
# ======================================================

df = pd.DataFrame({
    # Surface Roughness
    "Surface_Roughness_Actual": surface_data["Exp_value"],
    **{f"Surface_Roughness_{m}": surface_data[m] for m in surface_data if m != "Exp_value"},

    # MRR
    "MRR_Actual": mrr_data["Exp_value"],
    **{f"MRR_{m}": mrr_data[m] for m in mrr_data if m != "Exp_value"},

    # Kerf
    "Kerf_Actual": kerf_data["Exp_value"],
    **{f"Kerf_{m}": kerf_data[m] for m in kerf_data if m != "Exp_value"},
})

# Add Ensemble columns separately
df["Surface_Roughness_Ensemble"] = surface_data["Ensemble"]
df["MRR_Ensemble"] = mrr_data["Ensemble"]
df["Kerf_Ensemble"] = kerf_data["Ensemble"]


# ======================================================
# STEP 2: Compute metrics
# ======================================================

targets = ["Surface_Roughness", "MRR", "Kerf"]
models = ["Ridge", "RandomForest", "XGBoost", "NeuralNet", "KNN", "LSTM","Ensemble"]

results = {}

for target in targets:
    actual_col = f"{target}_Actual"
    y_true = df[actual_col]

    for model in models:
        pred_col = f"{target}_{model}"
        if pred_col not in df.columns:
            print(f"⚠ Warning: Missing predictions for {pred_col}")
            continue

        y_pred = df[pred_col]

        # Compute metrics WITHOUT rounding
        mse = mean_squared_error(y_true, y_pred)
        mae = mean_absolute_error(y_true, y_pred)
        r2 = r2_score(y_true, y_pred)

        results.setdefault(f"{target} – MSE", {})[model] = mse
        results.setdefault(f"{target} – MAE", {})[model] = mae
        results.setdefault(f"{target} – R²", {})[model] = r2

# ======================================================
# STEP 3: Save results to Excel
# ======================================================

results_df = pd.DataFrame(results).T
results_df = results_df[models]  # keep columns in model order
output_file = "Formatted_Metrics_Report.xlsx"
results_df.to_excel(output_file, index=True)

print(f"\n✅ Metrics saved to '{output_file}' with full precision!\n")
print(results_df)

