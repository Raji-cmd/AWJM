# === Features/Models ===
features = ['Ridge', 'RandomForest', 'ElasticNet', 'SVR', 'Ensemble']


# === Plot Function (auto-scaled range) ===
def plot_model_predictions(df, target_name):
    plt.figure(figsize=(12, 6))
    df_melt = df.melt(id_vars=['Exp.value'], value_vars=features,
                      var_name='Model', value_name='Predicted Value')

    sns.lineplot(data=df_melt, x='Exp.value', y='Predicted Value', hue='Model', marker='o')
    plt.title(f"Random Forest Predictions vs Experimental Values\n{target_name}",
              fontsize=16, fontweight='bold', pad=15)
    plt.xlabel("Experimental Values", fontsize=13, fontweight='bold')
    plt.ylabel("Predicted Values", fontsize=13, fontweight='bold')
    plt.xticks(fontsize=11, fontweight='bold')
    plt.yticks(fontsize=11, fontweight='bold')
    plt.grid(True, alpha=0.3)
    plt.legend(title='Model', fontsize=11, title_fontsize=12)
    plt.tight_layout()
    plt.show()


# === Generate Plots for Each Dataset ===
plot_model_predictions(df_surface, "Surface Roughness (µm)")
plot_model_predictions(df_mrr, "Material Removal Rate (mm³/min)")
plot_model_predictions(df_kerf, "Kerf Angle (°)")